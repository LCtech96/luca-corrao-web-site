"use client"

import { useTheme } from "next-themes"
import { Moon, Sun } from "lucide-react"
import { Button } from "@/components/ui/button"
import { useEffect, useState } from "react"

export function ThemeToggle() {
  const { theme, setTheme } = useTheme()
  const [mounted, setMounted] = useState(false)

  // Evita hydration mismatch
  useEffect(() => {
    setMounted(true)
  }, [])

  if (!mounted) {
    return (
      <Button
        variant="ghost"
        size="icon"
        className="text-gray-300 hover:text-cyan-400 hover:bg-cyan-500/10 transition-colors duration-200"
      >
        <Sun className="w-5 h-5" />
      </Button>
    )
  }

  const isDark = theme === "dark"

  return (
    <Button
      variant="ghost"
      size="icon"
      onClick={() => setTheme(isDark ? "light" : "dark")}
      className="text-gray-300 dark:text-gray-300 hover:text-cyan-400 dark:hover:text-cyan-400 hover:bg-cyan-500/10 transition-colors duration-200"
      title={isDark ? "ModalitÃ  Chiara" : "ModalitÃ  Scura"}
    >
      {isDark ? (
        <Sun className="w-5 h-5" />
      ) : (
        <Moon className="w-5 h-5" />
      )}
    </Button>
  )
}

